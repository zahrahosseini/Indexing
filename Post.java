
public class Post {

	public String doc;
	public int tfd;
	
	public Post(String _doc, int _tdf){
		doc=_doc;
		tfd=_tdf;
		
	}
	
	
	public String getDoc() {
		return doc;
	}
	public void setDoc(String doc) {
		this.doc = doc;
	}
	public int getTfd() {
		return tfd;
	}
	public void setTfd(int tfd) {
		this.tfd = tfd;
	}
	
}
